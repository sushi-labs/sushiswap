import type { VariablesOf } from 'gql.tada'

import { type RequestOptions, request } from 'src/lib/request.js'
import { SUSHI_DATA_API_HOST } from '../../data-api-host.js'
import { graphql } from '../../graphql.js'
import { SUSHI_REQUEST_HEADERS } from '../../request-headers.js'

export const CreatePerpsSushiReferralCodeMutation = graphql(
  `
    mutation CreatePerpsSushiReferralCode($address: EvmAddress!) {
      perps {
        createReferralCode(address: $address) {
          id
          code
          ownerAddress
          isAutoGenerated
          createdAt
        }
      }
    }
  `,
)

export type CreatePerpsSushiReferralCode = VariablesOf<
  typeof CreatePerpsSushiReferralCodeMutation
>

export async function createPerpsSushiReferralCode(
  variables: CreatePerpsSushiReferralCode,
  options?: RequestOptions,
) {
  const url = `${SUSHI_DATA_API_HOST}/graphql`

  const result = await request(
    {
      url,
      document: CreatePerpsSushiReferralCodeMutation,
      variables,
      requestHeaders: SUSHI_REQUEST_HEADERS,
    },
    options,
  )

  if (result) {
    return result.perps.createReferralCode
  }

  throw new Error('No created perps sushi referral code')
}

export type CreatedPerpsSushiReferralCode = Awaited<
  ReturnType<typeof createPerpsSushiReferralCode>
>
