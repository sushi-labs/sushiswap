import type { VariablesOf } from 'gql.tada'

import { type RequestOptions, request } from 'src/lib/request.js'
import { SUSHI_DATA_API_HOST } from '../../data-api-host.js'
import { graphql } from '../../graphql.js'
import { SUSHI_REQUEST_HEADERS } from '../../request-headers.js'

export const PerpsSushiReferralOverviewQuery = graphql(
  `
    query PerpsSushiReferralOverview($address: EvmAddress!) {
      perps {
        referralOverview(address: $address) {
          activeFeeLevels {
            level
            shareBps
          }
          address
          hasRedeemedReferralCode
          referredUserCount
          totalEarnedFees
          primaryReferralCode {
            id
            code
            ownerAddress
            isAutoGenerated
            createdAt
          }
        }
      }
    }
  `,
)

export type GetPerpsSushiReferralOverview = VariablesOf<
  typeof PerpsSushiReferralOverviewQuery
>

export async function getPerpsSushiReferralOverview(
  variables: GetPerpsSushiReferralOverview,
  options?: RequestOptions,
) {
  const url = `${SUSHI_DATA_API_HOST}/graphql`

  const result = await request(
    {
      url,
      document: PerpsSushiReferralOverviewQuery,
      variables,
      requestHeaders: SUSHI_REQUEST_HEADERS,
    },
    options,
  )

  if (result) {
    return result.perps.referralOverview
  }

  throw new Error('No perps sushi referral overview')
}

export type PerpsSushiReferralOverview = Awaited<
  ReturnType<typeof getPerpsSushiReferralOverview>
>
