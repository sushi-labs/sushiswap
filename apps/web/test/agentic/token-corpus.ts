function token(
  chainId: TestedChainId,
  address: EvmAddress,
  symbol: string,
  name: string,
  decimals: number,
  behaviors: readonly TokenBehavior[],
  coverage: TokenCoverage,
  confidence: TokenConfidence,
  source: TestedToken['source'] = 'sushi-config',
  notes?: string,
): TestedToken {
  return {
    chainId,
    address,
    symbol,
    name,
    decimals,
    behaviors,
    coverage,
    confidence,
    funding: coverage === 'smoke' ? 'set-erc20-balance' : 'impersonate-holder',
    source,
    notes,
  }
}
