import { SUPPORTED_CHAIN_IDS } from 'config'
import { GetStaticProps, InferGetStaticPropsType } from 'next'
import { FC } from 'react'
import { SWRConfig, unstable_serialize } from 'swr'

import { ChartSection, Layout, PoolsFiltersProvider, TableSection } from '../components'
import { getBundles, getCharts, getPoolCount, getPools, getTokenCount, getTokens } from '../lib/api'

export const getStaticProps: GetStaticProps = async () => {
  const [pairs, tokens, charts, poolCount, tokenCount, bundles] = await Promise.all([
    getPools(),
    getTokens(),
    getCharts(),
    getPoolCount(),
    getTokenCount(),
    getBundles(),
  ])
  return {
    props: {
      fallback: {
        [unstable_serialize({
          url: '/analytics/api/pools',
          args: {
            sorting: [
              {
                id: 'liquidityUSD',
                desc: true,
              },
            ],
            selectedNetworks: SUPPORTED_CHAIN_IDS,
            pagination: {
              pageIndex: 0,
              pageSize: 20,
            },
            query: '',
            extraQuery: '',
          },
        })]: pairs,
        [unstable_serialize({
          url: '/analytics/api/tokens',
          args: {
            sorting: [
              {
                id: 'liquidityUSD',
                desc: true,
              },
            ],
            selectedNetworks: SUPPORTED_CHAIN_IDS,
            pagination: {
              pageIndex: 0,
              pageSize: 20,
            },
            query: '',
            extraQuery: '',
          },
        })]: tokens,

        [unstable_serialize({
          url: '/analytics/api/charts',
          args: {
            selectedNetworks: SUPPORTED_CHAIN_IDS,
          },
        })]: charts,
        [`/analytics/api/pools/count`]: poolCount,
        [`/analytics/api/tokens/count`]: tokenCount,
        [`/analytics/api/bundles`]: bundles,
      },
    },
  }
}

const Index: FC<InferGetStaticPropsType<typeof getStaticProps>> = ({ fallback }) => {
  return (
    <SWRConfig value={{ fallback }}>
      <_Index />
    </SWRConfig>
  )
}

const _Index = () => {
  return (
    <Layout>
      <div className="flex flex-col gap-10">
        <section className="flex flex-col gap-6 lg:flex-row">
          <div className="max-w-md space-y-4">
            <h2 className="text-2xl font-semibold text-slate-50">Sushi Analytics</h2>
            <p className="text-slate-300">
              Analytics platform for tracking the liquidity, volume, and fees generated by Sushi products.
            </p>
          </div>
        </section>
        <PoolsFiltersProvider>
          <ChartSection />
          <TableSection />
        </PoolsFiltersProvider>
      </div>
    </Layout>
  )
}

export default Index
